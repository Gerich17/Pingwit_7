package pl.pingwit.homework2;

public class Task2 {
    public static void main(String[] args) {
        // "77", "12587", "-78945627", "123.6789", "-8965.876655"
        // byte, short, int, float, double
        byte myParsedByte = Byte.parseByte("77");
        System.out.println(myParsedByte);
        short myParsedShort = Short.parseShort("12587");
        System.out.println(myParsedShort);
        int myParsedInteger = Integer.parseInt("-78945627");
        System.out.println(myParsedInteger);
        float myParsedFloat = Float.parseFloat("123.6789");
        System.out.println(myParsedFloat);
        double myParsedDouble = Double.parseDouble("-8965.876655");
        System.out.println(myParsedDouble);






    }
}
